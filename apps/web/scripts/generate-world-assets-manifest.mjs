import { open, readFile, readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const dirname = path.dirname(fileURLToPath(import.meta.url));
const appRoot = path.resolve(dirname, '..');
const decorRoot = path.join(appRoot, 'public/assets/build/decor');
const categoriesConfigPath = path.join(appRoot, 'decor-categories.json');
const categoriesOutput = path.join(appRoot, 'components/world/scene/assets/categories.generated.ts');
const decorOutput = path.join(appRoot, 'components/world/scene/assets/decor.generated.ts');

async function pngDimensions(filePath) {
  const buf = Buffer.alloc(24);
  const fh = await open(filePath, 'r');
  try {
    await fh.read(buf, 0, 24, 0);
  } finally {
    await fh.close();
  }
  return { w: buf.readUInt32BE(16), h: buf.readUInt32BE(20) };
}

function isVisiblePng(file) {
  return (
    file.endsWith('.png') &&
    !file.startsWith('.') &&
    !file.startsWith('_') &&
    !file.endsWith('_top.png') &&
    !file.endsWith('_trunk.png')
  );
}

function labelFor(id) {
  return id
    .split('_')
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(' ');
}

function quote(value) {
  return `'${value.replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`;
}

async function loadCategoryConfig() {
  try {
    return JSON.parse(await readFile(categoriesConfigPath, 'utf8'));
  } catch {
    return {};
  }
}

async function buildDecor() {
  const categoryConfig = await loadCategoryConfig();

  const allFolders = (await readdir(decorRoot, { withFileTypes: true }))
    .filter((e) => e.isDirectory() && !e.name.startsWith('.') && !e.name.startsWith('_'))
    .map((e) => e.name);

  const configuredOrder = Object.keys(categoryConfig).filter((c) => allFolders.includes(c));
  const unknown = allFolders.filter((f) => !configuredOrder.includes(f)).sort();
  const folders = [...configuredOrder, ...unknown];

  const entries = [];
  for (const folder of folders) {
    const files = (await readdir(path.join(decorRoot, folder)))
      .filter(isVisiblePng)
      .sort((a, b) => a.localeCompare(b));

    for (const file of files) {
      const id = file.replace(/\.png$/, '');
      const filePath = path.join(decorRoot, folder, file);
      const { w, h } = await pngDimensions(filePath);
      if (folder !== 'rooms') {
        if (w % 16 !== 0 || h % 16 !== 0) continue;
        if (id.includes('_example') || id.includes('_palace')) continue;
      }
      entries.push({ id, label: labelFor(id), category: folder, file: `/assets/build/decor/${folder}/${file}` });
    }
  }

  return { entries, folders, categoryConfig };
}

function renderCategories({ folders, categoryConfig }) {
  const categoriesLiteral = folders.map((f) => quote(f)).join(', ');
  const labelsEntries = folders
    .map((f) => `  ${quote(f)}: ${quote(categoryConfig[f] ?? labelFor(f))}`)
    .join(',\n');
  const orderLiteral = folders.map((f) => quote(f)).join(', ');

  return `// Generated by apps/web/scripts/generate-world-assets-manifest.mjs.
// Do not edit by hand — edit decor-categories.json or add folders to public/assets/build/decor/.
export const DECOR_CATEGORIES = [${categoriesLiteral}] as const;
export type DecorCategory = (typeof DECOR_CATEGORIES)[number];
export const DECOR_CATEGORY_LABELS: Record<DecorCategory, string> = {
${labelsEntries},
};
export const DECOR_CATEGORY_ORDER: ReadonlyArray<DecorCategory> = [${orderLiteral}];
`;
}

function renderDecor({ entries }) {
  const body = entries
    .map(
      (e) => `  {
    id: ${quote(e.id)},
    label: ${quote(e.label)},
    category: ${quote(e.category)},
    cells: [{ dx: 0, dy: 0, file: ${quote(e.file)} }],
  },`,
    )
    .join('\n');

  return `// Generated by apps/web/scripts/generate-world-assets-manifest.mjs.
// Do not edit by hand — add/remove PNG files in public/assets/build/decor/.
// @ts-nocheck
export const DECOR = [
${body}
];
`;
}

const result = await buildDecor();
await writeFile(categoriesOutput, renderCategories(result));
await writeFile(decorOutput, renderDecor(result));
console.log(
  `Generated ${result.entries.length} decor assets across ${result.folders.length} categories.`,
);
