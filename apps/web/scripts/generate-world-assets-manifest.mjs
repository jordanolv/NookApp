import { readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const dirname = path.dirname(fileURLToPath(import.meta.url));
const appRoot = path.resolve(dirname, '..');
const decorRoot = path.join(appRoot, 'public/assets/build/decor');
const outputFile = path.join(appRoot, 'components/world/scene/assets/decor.generated.ts');

function isVisiblePng(file) {
  return (
    file.endsWith('.png') &&
    !file.startsWith('.') &&
    !file.startsWith('_') &&
    !file.endsWith('_top.png') &&
    !file.endsWith('_trunk.png')
  );
}

function labelFor(id) {
  return id
    .split('_')
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(' ');
}

function quote(value) {
  return `'${value.replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`;
}

async function buildDecor() {
  const entries = [];
  const folders = (await readdir(decorRoot, { withFileTypes: true }))
    .filter(
      (entry) => entry.isDirectory() && !entry.name.startsWith('.') && !entry.name.startsWith('_'),
    )
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b));

  for (const folder of folders) {
    const files = (await readdir(path.join(decorRoot, folder)))
      .filter(isVisiblePng)
      .sort((a, b) => a.localeCompare(b));

    for (const file of files) {
      const id = file.replace(/\.png$/, '');
      entries.push({
        id,
        label: labelFor(id),
        category: folder,
        file: `/assets/build/decor/${folder}/${file}`,
      });
    }
  }

  return entries;
}

function render(entries) {
  const body = entries
    .map(
      (entry) => `  {
    id: ${quote(entry.id)},
    label: ${quote(entry.label)},
    category: ${quote(entry.category)},
    cells: [{ dx: 0, dy: 0, file: ${quote(entry.file)} }],
  },`,
    )
    .join('\n');

  return `// Generated by apps/web/scripts/generate-world-assets-manifest.mjs.
// Do not edit by hand. Add/remove PNG files in public/assets/build/decor instead.
import type { DecorEntry } from './manifest';

export const DECOR: ReadonlyArray<DecorEntry> = [
${body}
];
`;
}

const decor = await buildDecor();
await writeFile(outputFile, render(decor));
console.log(`Generated ${path.relative(appRoot, outputFile)} with ${decor.length} decor assets.`);
